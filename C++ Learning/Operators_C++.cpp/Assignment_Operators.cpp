#include <iostream>
using namespace std;

int main(){
    // Assignment Operators

    // 1. = Operator
    int p = 20;
    cout << p << endl;

    // 2. += Operator
    int q = 15;
    q += 7;  // q = q + 7
    cout << q << endl;

    // 3. -= Operator
    int r = 7;
    r -= 5;  // r = r - 5
    cout << r << endl;

    // 4. *= Operator
    int s = 9;
    s *= 2;  // s = s * 2
    cout << s << endl;

    // 5. / = Operator
    int t = 24;
    t /= 12;  // t = t / 12
    cout << t << endl;
}