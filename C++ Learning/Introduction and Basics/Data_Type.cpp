#include <iostream>
using namespace std;

int main(){
    // Integer Data Type 
    int score = 95;
    cout << "score is: " << score <<endl;

    // Float Data Type
    float Percentage = 84.58;
    cout << "Percentage is: " << Percentage <<endl;

    // Double Data Type
    double Percentage_2 = 57.98588;
    cout << "Percentage_2 is: " << Percentage_2 <<endl;

    // Char Data Type
    char My_Char = 'k';
    cout << "My_Char is: " << My_Char <<endl;

    // String Data Type
    string Name = "Kashish Pal";
    cout << "My Name is: " << Name <<endl;
    
    // Boolean Data Type
    bool Answer = true;
    cout << "My Answer is: " << Answer <<endl;
}