#include <iostream>
#include <math.h>
using namespace std;

// Question- Find 5 inbuilt function in c++ like min or max etc and write a code around them.
int main()
{
    // 1st InBuilt Function(min)
    int x = 23, y = 52;
    cout << min(x, y) << endl;

    // 2nd InBuilt Function(max)
    int x1 = 568, y1 = 36;
    cout << max(x, y) << endl;

    // 3rd InBuilt Function(abs)
    int x2 = -95;
    cout << abs(x2) << endl;

    // 4th InBuilt Function(sqrt)
    double x3 = 25.0;
    cout << sqrt(x3) << endl;

    // 5th InBuilt Function(tolower, toupper)
    char ch = 'A';
    char lowercaseCh = tolower(ch); // lowercaseCh will be 'a'
    char uppercaseCh = toupper(ch); // uppercaseCh will be '
}